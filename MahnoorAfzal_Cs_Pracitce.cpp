#include <iostream>





int main(){

	int w = 20;
	int h = 60;
	
	std::cout << "Width of Rectangle = ";
	std::cout << w;
	std::cout << " meters.\n";
	
	std::cout << "Length of Rectangle = ";
	std::cout << h;
	std::cout << " meters.\n";
	
	std::cout << "-------------------------------------\n";
	std::cout << "Area of Rectangle is : ";
	std::cout << w*h ;
	std::cout << " meters\n";
	
	std::cout << "Parameter of Rectangle : ";
	std::cout <<  2*(w+h);
	std::cout << " meters\n";
	
	std::cout << "-------------------------------------\n";



	
	return 0;
}













